package collection;

/**
 * The type Car.
 */
public class VehiclePublicity {
    private boolean cool;

    /**
     * Instantiates a new Car.
     */
    public VehiclePublicity(){}

    /**
     * Instantiates a new Car.
     *
     * @param cool the cool
     */
    public VehiclePublicity(boolean cool){
        this.cool = cool;
    }

    /**
     * Get status boolean.
     *
     * @return the boolean
     */
    public boolean getStatus(){
        return cool;
    }


}
